/** Widen `as const` literals so locale files can use translated strings. */
type DeepString<T> = T extends string
  ? string
  : T extends ReadonlyArray<infer U>
    ? DeepString<U>[]
    : T extends object
      ? { [K in keyof T]: DeepString<T[K]> }
      : T;

